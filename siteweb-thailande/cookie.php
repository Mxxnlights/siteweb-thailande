    <h2>Qu'est-ce qu'un cookie ?</h2>
    <p>Un cookie est un petit fichier texte qui est stocké sur votre appareil (ordinateur, téléphone, tablette) lorsque vous visitez un site web. Il permet au site de se souvenir de vos actions et préférences (comme la langue, la taille de la police, et d'autres paramètres d'affichage) pendant un certain temps.</p>
